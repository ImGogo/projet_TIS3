    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        String newId = Integer.toString( Integer.parseInt( dm.getFiches().get( dm.getFiches().size() - 1).getId() ) + 1 );
        FicheDeSoins f = new FicheDeSoins(
            (Patient) this.patientBox.getSelectedItem(), 
            (Medecin) this.medecinBox.getSelectedItem(), 
            new Date(
                    Integer.parseInt( (String) this.dayBox.getSelectedItem() ), 
                    Integer.parseInt( (String) this.monthBox.getSelectedItem() ), 
                    Integer.parseInt( (String) this.yearBox.getSelectedItem() ),
                    Integer.parseInt( (String) this.hourBox.getSelectedItem() ),
                    Integer.parseInt( (String) this.minuteBox.getSelectedItem() )
            ),
            newId
        );
        mainWindow.ajouterFiche(f);
        ModifyXMLFile.addFiches(f);
        ((Patient) this.patientBox.getSelectedItem()).ajouterFiche(f);
        this.dispose();
    }//GEN-LAST:event_addBtnActionPerformed
    
