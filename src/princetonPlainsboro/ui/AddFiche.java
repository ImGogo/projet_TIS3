    public final void initFields() {
        initializeYearBox();
        initDateFields();
        this.dayBox.setSelectedItem(this);
        DefaultComboBoxModel<Patient> modelPatient = new DefaultComboBoxModel<>( new Vector<>( dm.getPatients()) );
        this.patientBox.setModel(modelPatient);
        DefaultComboBoxModel<Medecin> modelMedecin = new DefaultComboBoxModel<>( new Vector<>( dm.getMedecins()) );
        this.medecinBox.setModel(modelMedecin);
        
    }
    public final void initializeYearBox() {
        java.time.LocalDate date = java.time.LocalDate.now();
        int nYears = date.getYear() - STARTING_YEAR + 1;
        
        for(int i = 0; i < nYears; i++) {
            this.yearBox.addItem(Integer.toString(i + STARTING_YEAR));
        }
        
    }
    public final void initDateFields() {
        java.time.LocalDateTime date = java.time.LocalDateTime.now();
        
        this.dayBox.setSelectedIndex( date.getDayOfMonth() - 1 );
        this.monthBox.setSelectedIndex( date.getMonth().getValue() - 1);
        this.yearBox.setSelectedIndex( date.getYear() - STARTING_YEAR);
        this.hourBox.setSelectedIndex( date.getHour() );
        this.minuteBox.setSelectedIndex( date.getMinute() );
    }
    
